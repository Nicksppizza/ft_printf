#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t			src_len;
	size_t			i;

	src_len = 0;
	i = 0;
	src_len = ft_strlen(src);
	if (dstsize != 0)
	{
		while (i < src_len && i < dstsize - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (src_len);
}
